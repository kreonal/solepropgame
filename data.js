// ── Release calendar ──────────────────────────────────────────────────────────
// Generates a shuffled per-brand pool from CATALOG (non-grail, has retail).
// Called once at new game start and stored in state.
// Nike/Jordan/Adidas drop every week. New Balance even weeks, Asics odd weeks.
export function generateReleaseCalendar() {
  const brands = ["Nike", "Jordan", "Adidas", "New Balance", "Asics"];
  const calendar = {};
  for (const brand of brands) {
    const pool = CATALOG.filter(s => !s.isGrail && s.retail && s.brand === brand);
    calendar[brand] = shuffle([...pool]);
  }
  return calendar;
}

export function generateWeeklyReleases(releaseCalendar, weekNumber) {
  const i = weekNumber - 1; // 0-based from first raffle week
  const pick = (brand) => {
    const pool = releaseCalendar[brand];
    if (!pool || pool.length === 0) return null;
    return pool[i % pool.length];
  };
  return [
    pick("Nike"),
    pick("Jordan"),
    pick("Adidas"),
    weekNumber % 2 === 0 ? pick("New Balance") : pick("Asics"),
  ].filter(Boolean);
}

