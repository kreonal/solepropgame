// Win probability gated by hype tier
export function resolveRaffleWin(release) {
  const caps = { grail: 0.20, high: 0.42, medium: 0.65, low: 0.80 };
  const prob = Math.min(caps[release.hype] ?? 0.65, release.obtainability * 1.5);
  return Math.random() < prob;
}

// Hidden market price, revealed only on acquisition
export function resolveRaffleMarket(release) {
  const mult = release.baseMultMin + Math.random() * (release.baseMultMax - release.baseMultMin);
  return Math.round(release.retail * mult);
}

